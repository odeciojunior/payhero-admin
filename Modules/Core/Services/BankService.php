    /**
     * @param $countryCode
     * @return array
     */
    public function getBanks($countryCode)
    {
        switch ($countryCode) {
            case 'BR':
                return $this->brazilianBanks();
            case 'US':
                return $this->usaBanks();
            case 'PT':
                return $this->portugueseBanks();
            default:
                return [];
        }
    }

    /**
     * @param $bankCode
     * @param string|null $country
     * @return array
     */
    public function getBankName($bankCode, $country = null)
    {
        if($country == 'BR' || is_null($country)) {
            foreach ($this->brazilianBanks() as $bank) {
                if ($bank['code'] == $bankCode) {
                    return $bank['name'];
                }
            }
        }

        if($country == 'US' || is_null($country)) {
            foreach ($this->usaBanks() as $bank) {
                if ($bank['code'] == $bankCode) {
                    return $bank['name'];
                }
            }
        }

        if($country == 'PT' || is_null($country)) {
            foreach ($this->portugueseBanks() as $bank) {
                if ($bank['code'] == $bankCode) {
                    return $bank['name'];
                }
            }
        }

        return null;
    }

